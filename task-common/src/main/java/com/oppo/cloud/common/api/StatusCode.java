package com.oppo.cloud.common.api;

/**
 * 常用API返回对象
 */
public enum StatusCode implements IErrorCode {

    SUCCESS(200, "操作成功"),

    UNAUTHORIZED(401,"未授权"),
    FAILED(500, "操作失败"),
    VALIDATE_FAILED(404, "参数检验失败");

    private int code;
    private String msg;

    private StatusCode(int code, String msg) {
        this.code = code;
        this.msg = msg;
    }

    @Override
    public int getCode() {
        return code;
    }

    @Override
    public String getMsg() {
        return msg;
    }
}
